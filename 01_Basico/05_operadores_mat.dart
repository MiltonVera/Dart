  int a = 10+5; // + 15
  a = 20-10;    // - 10
  a = 20*2;     // * 20

  double b = 10/2; // / 5

  b = 10.0%3;      // % 1 El sobrante de la división
  b = -b;          // -expr Es usado para cambiar el valor de la expresion

  int c = 10 ~/ 3; // ~/  3  Division comun y corriente pero solo la parte entera(Division entera)

  int d = 1;

  d++;             // ++ 2
  d--;             // -- 1

